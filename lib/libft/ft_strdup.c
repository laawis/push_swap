#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*s1;
	char	*s2;
	int		i;

	if (!s)
		return (NULL);
	s1 = (char *)s;
	i = 0;
	s2 = malloc(sizeof(char) * (ft_strlen(s1) + 1));
	if (!s2)
		return (NULL);
	while (s1[i])
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
